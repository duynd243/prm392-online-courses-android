package com.mmc.models;

public class Mentor {
    private long id;
    private String fullName;
    private String imageUrl;
    private String email;
    private String bio;

    public Mentor() {
    }

    public long getId() {
        return id;
    }

    public String getFullName() {
        return fullName;
    }

    public String getImageUrl() {
        return imageUrl;
    }

    public String getEmail() {
        return email;
    }

    public String getBio() {
        return bio;
    }
}
